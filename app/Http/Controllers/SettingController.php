        // Check the user's role
        $userRole = auth()->user()->roles;
        // Return the corresponding view based on the role
        if ($userRole == 1) {
            // User is a car owner
            
            return view('owner.settings.index');
            
        } elseif ($userRole == 2) {
            // User is a customer
            $user = auth()->user();
            $bookings = Booking::where('user_id', $user->id)->get();
            return view('customer.settings.index',compact('bookings'));
        }
    
    }

